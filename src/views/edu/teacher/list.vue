<template>
  <div class="app-container">
    <!--查询表单-->
    <el-form :inline="true" class="demo-form-inline">
      <el-form-item>
        <el-input v-model="teacherQuery.name" placeholder="讲师名" />
      </el-form-item>

      <el-form-item>
        <el-select
          v-model="teacherQuery.level"
          clearable
          placeholder="讲师头衔"
        >
          <el-option :value="1" label="高级讲师" />
          <el-option :value="2" label="首席讲师" />
        </el-select>
      </el-form-item>

      <el-form-item label="添加时间">
        <el-date-picker
          v-model="teacherQuery.begin"
          type="datetime"
          placeholder="选择开始时间"
          value-format="yyyy-MM-dd HH:mm:ss"
          default-time="00:00:00"
        />
      </el-form-item>
      <el-form-item>
        <el-date-picker
          v-model="teacherQuery.end"
          type="datetime"
          placeholder="选择截止时间"
          value-format="yyyy-MM-dd HH:mm:ss"
          default-time="00:00:00"
        />
      </el-form-item>

      <el-button type="primary" icon="el-icon-search" @click="getList()"
        >查询</el-button
      >
      <el-button type="default" @click="resetData()">清空</el-button>
    </el-form>

    <!-- 表格 -->
    <el-table :data="list" border fit highlight-current-row>
      <el-table-column label="序号" width="70" align="center">
        <template slot-scope="scope">
          {{ (currentPage - 1) * limit + scope.$index + 1 }}
        </template>
      </el-table-column>

      <el-table-column prop="name" label="名称" width="80" />

      <el-table-column label="头衔" width="80">
        <template slot-scope="scope">
          {{ scope.row.level === 1 ? "高级讲师" : "首席讲师" }}
        </template>
      </el-table-column>

      <el-table-column prop="intro" label="资历" />

      <el-table-column prop="gmtCreate" label="添加时间" width="160" />

      <el-table-column prop="sort" label="排序" width="60" />

      <el-table-column label="操作" width="200" align="center">
        <template slot-scope="scope">
          <router-link :to="'/edu/teacher/edit/' + scope.row.id">
            <el-button type="primary" size="mini" icon="el-icon-edit"
              >修改</el-button
            >
          </router-link>
          <el-button
            type="danger"
            size="mini"
            icon="el-icon-delete"
            @click="removeDataById(scope.row.id)"
            >删除</el-button
          >
        </template>
      </el-table-column>
    </el-table>

    <!-- 分页 -->
    <el-pagination
      :current-page="currentPage"
      :page-size="limit"
      :total="total"
      style="padding: 30px 0; text-align: center"
      layout="total, prev, pager, next, jumper"
      @current-change="getList"
    />
  </div>
</template>

<script>
// 导入teacher.js代码
import teacher from "@/api/teacher/teacher.js";

export default {
  // 核心代码位置
  data() {
    // 定义变量和初始值
    return {
      list: null, // 查询之后接口返回的集合
      currentPage: 1, // 当前页
      total: 0,
      limit: 5, // 当前页的记录数
      teacherQuery: {}, // 条件封装对象
    };
  },
  created() {
    // 页面渲染之前执行，一般调用methods定义的方法
    this.getList();
  },
  methods: {
    // 创建具体的方法，调用teacher.js的方法

    // 讲师列表的方法，此处传入currentPage参数（默认为1），分页时element UI单击事件时自动传入currentPage
    getList(currentPage = 1) {
      this.currentPage = currentPage; // 将点击分页传入的currentPage赋值给data中定义的currentPage
      teacher
        .getTeacherListPage(this.currentPage, this.limit, this.teacherQuery)
        // 请求成功执行的方法
        .then((response) => {
          // response接口返回的数据
          this.list = response.data.rows;
          this.total = response.data.total;
        })
        // 请求失败执行的方法
        .catch((error) => {
          console.log(error);
        });
    },

    // 清空按钮执行方法，清空条件查询数据
    resetData() {
      // 由于表单查询时是双向绑定，此时将teacherQuery设置为空即可清空
      this.teacherQuery = {};
      // 默认查询讲师数据
      this.getList();
    },

    // 删除讲师
    removeDataById(id) {
      // element UI弹框
      this.$confirm("此操作将永久删除该文件, 是否继续?", "提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      })
        .then(() => {
          // 调用删除的方法
          teacher.removeById(id);
          this.$message({
            type: "success",
            message: "删除成功!",
          });
          // 再次调用分页条件查询
          this.getList(this.currentPage);
          this.getList(this.currentPage);
          this.getList(this.currentPage);
        })
        .catch(() => {
          this.$message({
            type: "info",
            message: "已取消删除",
          });
        });
    },
  },
};
</script>